package nameThatCelebrity;

public class NameThatCelebrity 
{
	public static void main(String[] args)
	{
		String s1 = "Allan Alda";
		String s2 = "John Wayne";
		String s3 = "Gregory Peck";
		System.out.println(s1.substring(0, s1.length()) + ">>>" + s1.substring(2, 7));
		System.out.println(s2.substring(0, s2.length()) + ">>>" + s2.substring(2,7));
		System.out.println(s3.substring(0, s3.length()) + ">>>" + s3.substring(2, 9));
	}	
}
